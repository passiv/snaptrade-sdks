import { Snaptrade, SnaptradeAuth } from "./index";

/**
 * This file is only testing correctness of the generated
 * signatures. No runtime assertions are made.
 **/

async function authModeTypeCoverage() {
	// Section: client construction.
	// The SDK exposes only the auth modes listed in konfig.yaml for TypeScript.
	const commercialApiKeyClient = new Snaptrade({
		auth: SnaptradeAuth.commercialApiKey({
			consumerKey: "consumer_key",
			clientId: "client_id",
		}),
	});

	const personalApiKeyClient = new Snaptrade({
		auth: SnaptradeAuth.personalApiKey({
			consumerKey: "consumer_key",
			clientId: "client_id",
		}),
	});

	if (false) {
		// OAuth is present in the OpenAPI spec but is not listed in TypeScript authModes,
		// so the generated TS SDK must not expose an OAuth auth factory.
		// @ts-expect-error Personal OAuth auth is not exposed by this SDK config.
		SnaptradeAuth.personalOAuth({
			accessToken: "oauth_test_token",
		});

		// Commercial auth constructor requires the signing secret.
		// @ts-expect-error Commercial API key auth requires consumerKey for request signing.
		SnaptradeAuth.commercialApiKey({
			clientId: "client_id",
		});

		// Personal auth constructor requires the signing secret.
		// @ts-expect-error Personal API key auth requires consumerKey for request signing.
		SnaptradeAuth.personalApiKey({
			clientId: "client_id",
		});

		// Commercial auth constructor rejects generated signature input.
		SnaptradeAuth.commercialApiKey({
			consumerKey: "consumer_key",
			clientId: "client_id",
			// @ts-expect-error Signature is generated by request signing and is not a public constructor parameter.
			signature: "signature",
		});

		// Personal auth constructor rejects generated timestamp input.
		SnaptradeAuth.personalApiKey({
			consumerKey: "consumer_key",
			clientId: "client_id",
			// @ts-expect-error Timestamp is generated by request signing and is not a public constructor parameter.
			timestamp: "timestamp",
		});
	}

	// Section: Commercial API key auth.

	// Commercial auth can generate a connection URL when user credentials are provided.
	await commercialApiKeyClient.authentication.loginSnapTradeUser({
		userId: "user-1",
		userSecret: "secret-1",
	});

	// Commercial auth can call user-scoped API-key operations when user credentials are provided.
	await commercialApiKeyClient.accountInformation.listUserAccounts({
		userId: "user-1",
		userSecret: "secret-1",
	});

	await commercialApiKeyClient.accountInformation.getUserAccountBalance({
		accountId: "account-1",
		userId: "user-1",
		userSecret: "secret-1",
	});

	await commercialApiKeyClient.referenceData.symbolSearchUserAccount({
		accountId: "account-1",
		substring: "AAPL",
		userId: "user-1",
		userSecret: "secret-1",
	});

	// Commercial auth can fetch connection data when user credentials are provided.
	await commercialApiKeyClient.connections.listBrokerageAuthorizations({
		userId: "user-1",
		userSecret: "secret-1",
	});

	if (false) {
		// Commercial auth requires user credentials to generate a connection URL.
		// @ts-expect-error Commercial API key auth requires user credentials for connection URL generation.
		await commercialApiKeyClient.authentication.loginSnapTradeUser({});

		// Commercial auth requires user credentials for user-scoped account operations.
		// @ts-expect-error Commercial API key auth requires user credentials for user-scoped account operations.
		await commercialApiKeyClient.accountInformation.listUserAccounts({});

		// Commercial auth requires user credentials to fetch connection data.
		// @ts-expect-error Commercial API key auth requires user credentials for connection data.
		await commercialApiKeyClient.connections.listBrokerageAuthorizations({});
	}

	// Section: Personal API key auth.

	// Personal auth can generate a connection URL without user credentials.
	await personalApiKeyClient.authentication.loginSnapTradeUser();

	// Personal auth can call user-scoped API-key operations without user credentials.
	await personalApiKeyClient.accountInformation.listUserAccounts({});

	await personalApiKeyClient.accountInformation.getUserAccountBalance({
		accountId: "account-1",
	});

	await personalApiKeyClient.referenceData.symbolSearchUserAccount({
		accountId: "account-1",
		substring: "AAPL",
	});

	// Personal auth can fetch connection data without user credentials.
	await personalApiKeyClient.connections.listBrokerageAuthorizations();

	if (false) {
		// Personal auth must not accept user credentials when generating a connection URL.
		await personalApiKeyClient.authentication.loginSnapTradeUser({
			// @ts-expect-error Personal API key auth must not accept user credentials for connection URL generation.
			userId: "user-1",
			// @ts-expect-error Personal API key auth must not accept user credentials for connection URL generation.
			userSecret: "secret-1",
		});

		// Personal auth must not accept user credentials on user-scoped operations.
		await personalApiKeyClient.accountInformation.listUserAccounts({
			// @ts-expect-error Personal API key auth must not accept per-operation user credentials.
			userId: "user-1",
			// @ts-expect-error Personal API key auth must not accept per-operation user credentials.
			userSecret: "secret-1",
		});

		// Personal auth must not accept user credentials when fetching connection data.
		await personalApiKeyClient.connections.listBrokerageAuthorizations({
			// @ts-expect-error Personal API key auth must not accept user credentials for connection data.
			userId: "user-1",
			// @ts-expect-error Personal API key auth must not accept user credentials for connection data.
			userSecret: "secret-1",
		});
	}

	// Section: Global security inheritance.

	// Commercial auth can call parameterless operations that inherit top-level API-key security.
	await commercialApiKeyClient.authentication.listSnapTradeUsers();

	// Commercial auth can call body operations that inherit top-level API-key security.
	await commercialApiKeyClient.authentication.registerSnapTradeUser({
		userId: "user-1",
	});

	// Commercial auth can delete users and rotate their secrets.
	await commercialApiKeyClient.authentication.deleteSnapTradeUser({
		userId: "user-1",
	});
	await commercialApiKeyClient.authentication.resetSnapTradeUserSecret({
		userId: "user-1",
		userSecret: "secret-1",
	});

	if (false) {
		// Personal auth cannot call Commercial-only user lifecycle operations.
		// @ts-expect-error Listing partner users requires Commercial API key auth.
		await personalApiKeyClient.authentication.listSnapTradeUsers();
		// @ts-expect-error Registering a partner user requires Commercial API key auth.
		await personalApiKeyClient.authentication.registerSnapTradeUser({
			userId: "user-1",
		});
		// @ts-expect-error Deleting a partner user requires Commercial API key auth.
		await personalApiKeyClient.authentication.deleteSnapTradeUser();
		// @ts-expect-error Rotating a partner user secret requires Commercial API key auth.
		await personalApiKeyClient.authentication.resetSnapTradeUserSecret({
			userId: "user-1",
			userSecret: "secret-1",
		});
	}
}

void authModeTypeCoverage;
